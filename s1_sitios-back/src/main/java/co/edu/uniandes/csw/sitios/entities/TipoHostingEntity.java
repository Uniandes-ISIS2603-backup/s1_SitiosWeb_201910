package co.edu.uniandes.csw.sitios.entities;

import java.io.Serializable;
import javax.persistence.Entity;
import javax.persistence.OneToOne;
import uk.co.jemos.podam.common.PodamExclude;

/**
 *
 * @author estudiante
 */
@Entity
public class TipoHostingEntity extends BaseEntity implements Serializable {
    
    /*
* Nombre del tipoHosting
*/    
private String nombre;

/*
* Categoria del tipoHosting
*/
private String categoria;

@PodamExclude
@OneToOne
private PlataformaDeDespliegueEntity plataformaDeDespliegue;

public TipoHostingEntity(){
}
    /**
     * @return the nombre
     */
    public String getNombre() {
        return nombre;
    }

    /**
     * @param nombre the nombre to set
     */
    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    /**
     * @return the categoria
     */
    public String getCategoria() {
        return categoria;
    }

    /**
     * @param categoria the categoria to set
     */
    public void setCategoria(String categoria) {
        this.categoria = categoria;
    }

}
